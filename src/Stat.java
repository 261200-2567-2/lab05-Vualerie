public class Stat {
    private int str;
    private int vit;
    private int agi;
    private int Int;

    public Stat(int str, int vit, int agi, int Int) {

        this.str = str;
        this.vit = vit;
        this.agi = agi;
        this.Int = Int;
    }
    public int getStr() {
        return str;
    }
    public int getVit() {
        return vit;
    }
    public int getAgi() {
        return agi;
    }
    public int getInt() {
        return Int;
    }
}
